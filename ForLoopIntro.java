package day018;

public class ForLoopIntro {

    public static void main(String[] args) {

        // count 1 to 5
        for( int i = 1; i<=5; i++){
            System.out.println("Happy Birthday");
        }

        for( int counter = 10; counter >= 1; counter--){
            System.out.println("counter " + counter);
        }


    }
}
