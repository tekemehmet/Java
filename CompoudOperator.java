package day06;

public class CompoudOperator {

    public static void main(String[] args) {

        int studenOnline = 263 ;

        studenOnline = studenOnline + 5 ;
        System.out.println(studenOnline);

        studenOnline = studenOnline - 5;
        System.out.println(studenOnline);

        studenOnline = studenOnline*2;
        System.out.println(studenOnline);




        // +=, -= , *= , /= , %=

        studenOnline += 5;
        System.out.println(studenOnline);

        studenOnline -= 5;
        System.out.println(studenOnline);

        studenOnline *= 2;
        System.out.println(studenOnline);

        studenOnline /= 2;
        System.out.println(studenOnline);

        studenOnline %= 50;
        System.out.println(studenOnline);


    }
}
