package day06;

public class Remainder {

    public static void main(String[] args) {

        // + - * /

        System.out.println(5/2);  // Result is 2  because of variable type is int

        System.out.println(5.0/2); // Result 2.5  Variable type is double

        System.out.println(5.0/2f);

        //  % Remainder( Kalan degeri bulma)

        System.out.println(5%2); // Result is 1, because remainder is 1
        System.out.println(99%10);    // Remainder is 9
        System.out.println(100%10);

    }
}
