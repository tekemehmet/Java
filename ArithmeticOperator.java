package day04;

public class ArithmeticOperator {

    public static void main(String[] args) {

        System.out.println(5+3);
        System.out.println(5-3);
        System.out.println(5*3);
        System.out.println(5/3);

        System.out.println(5+3.0d);
        System.out.println(5-3.0d);
        System.out.println(5*3.0d);
        System.out.println(5/3.0d);
        System.out.println(5/3.0f);

        double a = 5;
        double b = 14.96d;
        double result = a*b;
        System.out.println(result);
    }
}
