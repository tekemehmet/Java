package day018;

public class DoWhileIntro {

    public static void main(String[] args) {

        //   do {
        //   Take some action here and repeat it
        //  } while (some condition is true)

        int x = 1;

        do {
            System.out.println(x);
            ++x;
        } while (x < 10); // semicolon

        System.out.println("****************************************");
        int y = 5 ;
        do {
            System.out.println( y );
            --y;

        } while (y >=0 ) ;



    }



    }

