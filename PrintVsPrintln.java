package day03;

public class PrintVsPrintln {

    public static void main(String[] args) {

        System.out.println("Hello\t World line1");
        System.out.println("Hello\n World line2");
        System.out.println("Hello\' World line3");
        System.out.print("Hello\" World4");
        System.out.println("Hello World5");
        System.out.println("I like the book \"Java\" ");


    }



}
